class CriterionSpine:
    def __init__(
        self,
        spineNo: str,
        title: str,
        director: str,
        country: str,
        year: str,
        cover: str,
        url: str,
        description: str,
        runtime: str,
        language: str,
    ):
        self.spineNo = spineNo
        self.title = title
        self.director = director
        self.country = country
        self.year = year
        self.cover = cover
        self.url = url
        self.description = description
        self.runtime = runtime
        self.language = language
